SELECT YEAR(DATA_rel) ANO,
		MONTH(DATA_rel) MES,
		DAY(DATA_rel) as DIA,
		COUNT(DATA_rel) QTDE
	FROM SQVI_ESTOQUE
	WHERE DATA_rel >= '01/01/2022'
	GROUP BY YEAR(DATA_rel), MONTH(DATA_rel), DAY(DATA_rel)
	ORDER BY 1, 2, 3;


